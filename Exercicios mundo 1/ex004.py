d = input('Digite algo: ')

print('O tipo primitivo é {}'.format(type(d)))

print('Tem espaços ? {}'.format(d.isspace()))

print('É alfabetico ? {}'.format(d.isalpha()))

print('É alfanúmerico? {}'.format(d.isalnum()))

print('É um número ? {}'.format(d.isnumeric()))

print('Todas as letras são maiúscula ? {}'.format(d.isupper()))

print('Todas as letras são minúscula ? {}'.format(d.islower()))





